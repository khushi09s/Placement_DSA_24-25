package Lec1;

public class Arithmetic_Operator {
    public static void main(String[] args) {
        int a = 9;
        int b = 11;
        int c = a + b;
        System.out.println(c);
        c = a - b;
        System.out.println(c);
        int d = a * b;
        System.out.println(d);
        int e = a / b;
        System.out.println(e);
    }
}